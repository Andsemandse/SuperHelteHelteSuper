public class Superhero {
private String navn;
private String superkraft;
private int fødselsdag;
private String SandeNavn;
private double højde;
private int vægt;


//Konstruktør
    public Superhero (String navn, String superkraft, int fødselsdag, String SandeNavn, double højde, int vægt){
        this.navn = navn;
        this.superkraft = superkraft;
        this.fødselsdag = fødselsdag;
        this.SandeNavn = SandeNavn;
        this.højde = højde;
        this.vægt = vægt;
    }


}
